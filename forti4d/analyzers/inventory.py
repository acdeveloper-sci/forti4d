# Executable statements also trigger (IF, DO, CALL, etc.), but those
# are captured by flags or discarded. These are the critical declarative ones.


def compile_list(words):
    if not words:
        return None
    pattern = r"\b(" + "|".join(re.escape(p) for p in words) + r")\b"
    return re.compile(pattern, re.IGNORECASE)


RE_LEGACY = compile_list(LEGACY_LIST)
RE_IO = compile_list(IO_LIST)
RE_CUSTOM = compile_list(CUSTOM_LIST)

# Master Pattern for Implicit Start
RE_IMPLICIT_START = compile_list(TRIGGERS_SPECIFICATION)

# If patterns.py does not have RE_END_INTERFACE, the ideal is to add it there.
# But for the purposes of this script, we define a robust one that accepts optional names:
# This regex matches: "END", spaces, "INTERFACE", and optionally more things (name)
RE_END_INTERFACE = re.compile(r"^\s*END\s*INTERFACE\b", re.IGNORECASE)


class DetectedUnit:

    def __init__(self, utype: str, name: str, start_line: int, parent: str = "GLOBAL"):
        self.type = utype
        self.name = name
        self.start_line = start_line
        self.parent = parent
        self.end_line = -1
        self.flags_legacy: Set[str] = set()
        self.flags_io: Set[str] = set()
        self.flags_custom: Set[str] = set()

    @property
    def total_lines(self):
        if self.end_line == -1:
            return "?"
        return self.end_line - self.start_line + 1


def mask_strings(text: str) -> str:
    text = re.sub(r"'[^']*'", "''", text)
    text = re.sub(r'"[^"]*"', '""', text)
    return text


